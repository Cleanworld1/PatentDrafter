"상기 표를 참조하면, NaOH 200 ppm 조건에서 Ca 및 Mg의 농도가 비교예 및 100 ppm 조건 대비 급격히 감소하였다. 이는 NaOH 투입량이 200 ppm에 도달함으로써 폐 NMP 내 Ca 및 Mg 성분이 침전물 형태로 본격적으로 전환되기 시작함을 의미한다. 반면, 500 ppm을 초과하는 조건에서는 금속 성분의 잔여 농도는 추가적으로 감소하였으나, 고형분 포집량이 감소하고 색도가 증가하여 침전물의 미세화 또는 분산화가 발생할 수 있음을 확인하였다."

